function GUIDB.Cache()
	GUI.Write("GUIDB " .. GUIDB.Version)
	for k, v in pairs(GUIDB.Items) do
		local tempName = k
		tempName = tempName:gsub("item_", "")
		local imageHandle = GUIDB.Images[tempName]
		if imageHandle == nil then
			imageHandle = Renderer.LoadImage(GUIDB.IconPath .. tempName .. ".png")
			GUIDB.Images[tempName] = imageHandle
		end
	end
	GUIDB.Images["recipe"] = Renderer.LoadImage(GUIDB.IconPath .. "recipe.png")

	for k, v in pairs(GUIDB.Heroes) do
		local tempName = k
		tempName = tempName:gsub("npc_dota_hero_", "")
		
		local imageHandle = GUIDB.Images[tempName]
		if imageHandle == nil then
			if tempName == "pangolier" or tempName == "dark_willow" then
				imageHandle = Renderer.LoadImage("~/Extra/" .. tempName .. ".png")
			else
				imageHandle = Renderer.LoadImage(GUIDB.HeroPath .. tempName .. ".png")
			end
			GUIDB.Images[tempName] = imageHandle
		end
		
		imageHandle = GUIDB.Images['mini_' .. tempName]
		if imageHandle == nil then
			GUIDB.Images['mini_' .. tempName] = Renderer.LoadImage(GUIDB.MiniHeroPath .. tempName .. ".png")
		end		
	end
	
	GUIDB.Images["star_circle"] = Renderer.LoadImage("~/Extra/star_circle.png")

	GUI.Write("GUIDB cached")
end

function GUIDB.Image(name)
	if string.match(name, "recipe_") then name = "recipe" end
	name = name:gsub("item_", "")
	name = name:gsub("npc_dota_hero_", "")
	
	return GUIDB.Images[name]
end

return GUIDB
